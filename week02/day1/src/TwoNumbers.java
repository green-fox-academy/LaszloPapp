public class TwoNumbers {

    public static void main(String[] args) {

        int a = 13;
        int b = 22;


        System.out.println(a+b);
        System.out.println(b-a);
        System.out.println(b*a);
        System.out.println(22/13.);
        System.out.println(b/a);
        System.out.println(b%a);
    }

}
